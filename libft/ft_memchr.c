#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*s2;
	size_t			i;

	s2 = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*s2 == (unsigned char)c)
			return ((void *)(s2));
		i++;
		s2++;
	}
	return (NULL);
}
